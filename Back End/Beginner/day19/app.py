@app.route('/signup/', methods=['POST', 'GET'])
def form():
    id = len(data)
    if request.method == 'POST':
        obj = {
            "id": len(data)+1,
            "name" : request.form['name'],
            "username" : request.form['username'],
            "email" : request.form['email'],
        }
        data.append(obj)
        save_db(data)
        return 'Data Successfully added'
    return render_template('form.html', id=id)

